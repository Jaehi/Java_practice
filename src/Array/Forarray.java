package Array;

public class Forarray {
    public static void main(String[] args) {
        String[] people = new String[] {"qq","ww","ee","rr"};
        for (String s : people){
            System.out.println(s);
        }

        int[] number = new int[] {1,2,3,4};
        for (int n : number){
            System.out.println(n);
        }
    }
}
